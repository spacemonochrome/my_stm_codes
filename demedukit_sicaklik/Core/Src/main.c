{
	double temperature;
	uint32_t adcval = 4096-analogValue;
	temperature = log((adcval*10000)/(4095-adcval));
	temperature = 1 / (0.001129148 + (0.000234125 + (0.0000000876741 * temperature * temperature)) * temperature);
	temperature = temperature - 273.15;
	return temperature;

}
